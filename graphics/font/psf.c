/* -----------------------------------------------------------------------
 * Load PSF2
 * --------------------------------------------------------------------- */

static PSFFont *load_psf2(const uint8_t *data, size_t size) {
    if (size < sizeof(PSF2Header)) return NULL;
    const PSF2Header *hdr = (const PSF2Header *)data;

    if (hdr->magic[0] != PSF2_MAGIC0 || hdr->magic[1] != PSF2_MAGIC1 ||
        hdr->magic[2] != PSF2_MAGIC2 || hdr->magic[3] != PSF2_MAGIC3)
        return NULL;

    uint32_t header_size    = read_u32_le((uint8_t *)&hdr->header_size);
    uint32_t num_glyph      = read_u32_le((uint8_t *)&hdr->num_glyph);
    uint32_t bytes_per_glyph= read_u32_le((uint8_t *)&hdr->bytes_per_glyph);
    uint32_t height         = read_u32_le((uint8_t *)&hdr->height);
    uint32_t width          = read_u32_le((uint8_t *)&hdr->width);
    uint32_t flags          = read_u32_le((uint8_t *)&hdr->flags);

    if (header_size > size) return NULL;
    size_t glyph_data_size = (size_t)num_glyph * bytes_per_glyph;
    if (header_size + glyph_data_size > size) return NULL;

    PSFFont *font = xmalloc(sizeof(PSFFont));
    if (!font) return NULL;

    font->version          = PSF_VERSION_2;
    font->width            = (int)width;
    font->height           = (int)height;
    font->bytes_per_glyph  = (int)bytes_per_glyph;
    font->num_glyphs       = (int)num_glyph;
    font->glyph_data_size  = glyph_data_size;
    font->owns_data        = 1;

    font->glyph_data = xmalloc(glyph_data_size);
    if (!font->glyph_data) { free(font); return NULL; }
    memcpy(font->glyph_data, data + header_size, glyph_data_size);

    if (flags & PSF2_HAS_UNICODE_TABLE) {
        const uint8_t *utab     = data + header_size + glyph_data_size;
        size_t         utab_len = size - header_size - glyph_data_size;

        int max_entries = (int)num_glyph * 4;
        font->unicode_table = xmalloc(sizeof(uint16_t) * max_entries * 2);
        font->unicode_table_size = 0;

        int glyph_idx = 0;
        size_t pos = 0;
        while (pos < utab_len && glyph_idx < (int)num_glyph) {
            while (pos + 1 < utab_len) {
                uint32_t cp = 0;
                uint8_t b0  = utab[pos];

                if (b0 == 0xFF) { pos++; break; }
                if (b0 == 0xFE) { pos++; continue; }

                if      ((b0 & 0x80) == 0x00) { cp = b0; pos += 1; }
                else if ((b0 & 0xE0) == 0xC0) {
                    if (pos+1 >= utab_len) break;
                    cp = ((b0&0x1F)<<6) | (utab[pos+1]&0x3F);
                    pos += 2;
                } else if ((b0 & 0xF0) == 0xE0) {
                    if (pos+2 >= utab_len) break;
                    cp = ((b0&0x0F)<<12) | ((utab[pos+1]&0x3F)<<6) |
                         (utab[pos+2]&0x3F);
                    pos += 3;
                } else { pos++; continue; }

                if (font->unicode_table_size + 2 <= max_entries * 2) {
                    font->unicode_table[font->unicode_table_size++] =
                        (uint16_t)cp;
                    font->unicode_table[font->unicode_table_size++] =
                        (uint16_t)glyph_idx;
                }
            }
            glyph_idx++;
        }
    }

    return font;
}

/* -----------------------------------------------------------------------
 * Load PSF1
 * --------------------------------------------------------------------- */

static PSFFont *load_psf1(const uint8_t *data, size_t size) {
    if (size < sizeof(PSF1Header)) return NULL;
    const PSF1Header *hdr = (const PSF1Header *)data;
    if (hdr->magic[0] != PSF1_MAGIC0 || hdr->magic[1] != PSF1_MAGIC1)
        return NULL;

    int num_glyphs = (hdr->mode & PSF1_MODE512) ? 512 : 256;
    int bpg        = hdr->charsize;
    size_t glyph_data_size = (size_t)num_glyphs * bpg;
    size_t header_size = sizeof(PSF1Header);

    if (header_size + glyph_data_size > size) return NULL;

    PSFFont *font = xmalloc(sizeof(PSFFont));
    if (!font) return NULL;

    font->version          = PSF_VERSION_1;
    font->width            = 8;
    font->height           = bpg;
    font->bytes_per_glyph  = bpg;
    font->num_glyphs       = num_glyphs;
    font->glyph_data_size  = glyph_data_size;
    font->owns_data        = 1;

    font->glyph_data = xmalloc(glyph_data_size);
    if (!font->glyph_data) { free(font); return NULL; }
    memcpy(font->glyph_data, data + header_size, glyph_data_size);

    return font;
}

/* -----------------------------------------------------------------------
 * Public load functions
 * --------------------------------------------------------------------- */

PSFFont *psf_load_from_mem(const uint8_t *data, size_t size) {
    if (!data || size < 4) return psf_builtin();

    if (data[0] == PSF2_MAGIC0 && data[1] == PSF2_MAGIC1 &&
        data[2] == PSF2_MAGIC2 && data[3] == PSF2_MAGIC3)
        return load_psf2(data, size);

    if (data[0] == PSF1_MAGIC0 && data[1] == PSF1_MAGIC1)
        return load_psf1(data, size);

    return psf_builtin();
}

PSFFont *psf_load(const char *path) {
    if (!path) return psf_builtin();

    FILE *f = fopen(path, "rb");
    if (!f) return psf_builtin();

    fseek(f, 0, SEEK_END);
    long sz = ftell(f);
    rewind(f);

    if (sz <= 0) { fclose(f); return psf_builtin(); }

    uint8_t *data = xmalloc((size_t)sz);
    if (!data) { fclose(f); return psf_builtin(); }

    if ((long)fread(data, 1, sz, f) != sz) {
        free(data); fclose(f); return psf_builtin();
    }
    fclose(f);

    PSFFont *font = psf_load_from_mem(data, (size_t)sz);
    free(data);
    return font;
}

void psf_free(PSFFont *font) {
    if (!font || font == &_builtin) return;
    if (font->owns_data) {
        free(font->glyph_data);
        free(font->unicode_table);
    }
    free(font);
}

/* -----------------------------------------------------------------------
 * Glyph access
 * --------------------------------------------------------------------- */

int psf_unicode_to_glyph(const PSFFont *font, uint32_t codepoint) {
    if (!font) return 0;

    if (font->unicode_table) {
        int n = font->unicode_table_size / 2;
        for (int i = 0; i < n; i++) {
            if (font->unicode_table[i * 2] == (uint16_t)codepoint)
                return (int)font->unicode_table[i * 2 + 1];
        }
    }

    /* fallback: direct index for ASCII */
    if (codepoint < (uint32_t)font->num_glyphs)
        return (int)codepoint;

    /* fallback: question mark */
    if ('?' < font->num_glyphs)
        return '?';

    return 0;
}

const uint8_t *psf_glyph(const PSFFont *font, uint32_t codepoint) {
    if (!font || !font->glyph_data) return NULL;
    int idx = psf_unicode_to_glyph(font, codepoint);
    if (idx < 0 || idx >= font->num_glyphs) idx = 0;
    return font->glyph_data + idx * font->bytes_per_glyph;
}

int psf_glyph_pixel(const PSFFont *font, int glyph_idx, int x, int y) {
    if (!font || !font->glyph_data) return 0;
    if (glyph_idx < 0 || glyph_idx >= font->num_glyphs) return 0;
    if (x < 0 || y < 0 || x >= font->width || y >= font->height) return 0;

    const uint8_t *glyph = font->glyph_data +
                            glyph_idx * font->bytes_per_glyph;
    int bytes_per_row = (font->width + 7) / 8;
    uint8_t byte = glyph[y * bytes_per_row + x / 8];
    return (byte >> (7 - (x & 7))) & 1;
}

int psf_char_pixel(const PSFFont *font, uint32_t codepoint, int x, int y) {
    if (!font) return 0;
    int idx = psf_unicode_to_glyph(font, codepoint);
    return psf_glyph_pixel(font, idx, x, y);
}